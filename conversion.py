# My Conversion Tools

def dollars2cents(dollars):
    cents = dollars * 100
    return cents

def Gallos2Liters(gallons):
    liter = gallons * 3.78541
    return liter

def hours2minutes(hours):
    minutes = hours * 60
    return minutes

def feet2inches(feet):
    inches = feet * 12
    return Inches

def Degrees2Radians(degrees):
    radians = pi() * degrees / 180
    return radians
